#operator ตัวดำเนินการ ที่ใช้ในการ ประมวลผลข้อมูล
#1.Arithmetic operator เครื่องหมายคำนวณ 
# (+,-,*,/,%mod,// floor,** eponetial)
print(10 + 3)
print(10 - 3)
print(10 * 3)
print(10 / 3)
print(10 % 3)  #หารเอาเศษ เลขจำนวนเต็มเท่านั้น
print(10 //3)  #หารเอาส่วนเ ลขจำนวนเต็มเท่านั้น
print(10 **3) 
print("---------")
# comparison operator เครื่องหมายเปรียบเทียบ
